    private void tabelmobilMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelmobilMouseClicked
        // TODO add your handling code here:
        try {
    koneksi();
    int row = tabelmobil.getSelectedRow();
    String tabel_klik = (tabelmobil.getModel().getValueAt(row, 0).toString());
    java.sql.Statement stm = con.createStatement();
    java.sql.ResultSet sql = stm.executeQuery("SELECT * FROM tbl_mobil WHERE kode_mobil = '" + tabel_klik + "'");
    
    if (sql.next()) {
                kodemobil.setText(sql.getString("kode_mobil"));
                namamobil.setText(sql.getString("nama_mobil"));
                merk.setSelectedItem(sql.getString("merk"));
                kapasitas.setText(sql.getString("kapasitas_penumpang"));
                stok.setText(sql.getString("stok"));
                harga.setText(sql.getString("harga"));
            }
} catch (Exception e) {
    // Bisa ditambahkan log error jika dibutuhkan
    e.printStackTrace();
}

    }//GEN-LAST:event_tabelmobilMouseClicked

