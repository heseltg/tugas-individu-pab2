private void koneksi() {
    try {
        Class.forName("com.mysql.jdbc.Driver");
        String url = "jdbc:mysql://localhost/dbhesekiel"; // url database
        String user = "root"; // user database
        String pass = ""; // password database
        con = DriverManager.getConnection(url, user, pass);
        statMobil = con.createStatement(rs.TYPE_SCROLL_SENSITIVE,rs.CONCUR_UPDATABLE);
        rs = statMobil.executeQuery("select * from tbl_mobil");
        
        } catch (Exception e) {
             JOptionPane.showMessageDialog(null, e);
             System.exit(0);
          }
    }

// Method untuk menampilkan data ke tabel
private void display() {
    try {
        String sql = "select kode_mobil, nama_mobil, merk, kapasitas_penumpang, harga  from tbl_mobil";
        pst = con.prepareStatement(sql);
        rs = pst.executeQuery();
        tabelmobil.setModel(DbUtils.resultSetToTableModel(rs));
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }
}

